function checkDataIntegrity() {
    const container = document.getElementById('dataIntegrityResults');
    container.classList.add('active');
    container.innerHTML = '<div class="info">üîç Verificando integridad de datos...</div>';
    
    systemLogger.info('Iniciando verificaci√≥n de integridad de datos');
    
    setTimeout(() => {
        const issues = [];
        
        try {
            const auctions = JSON.parse(localStorage.getItem('auctions') || '[]');
            auctions.forEach((auction, index) => {
                if (!auction.id || !auction.title || !auction.startingPrice) {
                    issues.push(`Subasta ${index}: Faltan campos obligatorios`);
                }
                if (auction.currentPrice < 0) {
                    issues.push(`Subasta ${auction.title}: Precio negativo`);
                }
            });
        } catch (e) {
            issues.push('Error al cargar subastas: ' + e.message);
        }

        try {
            const users = JSON.parse(localStorage.getItem('users') || '[]');
            const emails = new Set();
            users.forEach((user, index) => {
                if (!user.email || !user.name) {
                    issues.push(`Usuario ${index}: Faltan campos obligatorios`);
                }
                if (emails.has(user.email)) {
                    issues.push(`Email duplicado: ${user.email}`);
                }
                emails.add(user.email);
                
                if (user.tokens < 0) {
                    issues.push(`Usuario ${user.name}: Tokens negativos`);
                }
            });
        } catch (e) {
            issues.push('Error al cargar usuarios: ' + e.message);
        }

        let result = '';
        if (issues.length === 0) {
            result = '<div class="success">‚úÖ Integridad verificada: No se encontraron problemas</div>';
            systemLogger.info('Verificaci√≥n de integridad completada: Sin problemas');
        } else {
            result = '<div class="warning">‚ö†Ô∏è Se encontraron ' + issues.length + ' problemas:</div>';
            issues.forEach(issue => {
                result += '<div class="error">‚Ä¢ ' + issue + '</div>';
            });
            systemLogger.warning('Verificaci√≥n de integridad completada', { issues: issues });
        }
        
        container.innerHTML = result;
    }, 1500);
}

